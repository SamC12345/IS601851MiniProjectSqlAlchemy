import math

class Statistics:
    def __init__(self):
        pass

    def add(self,a,b):
        return a+b
    def subtract(self,a,b):
        return b-a
    def multiply(self,a,b):
        return a*b
    def divide(self,a,b):
        return b/a
    def square(self,a):
        return a*a
    def squareRoot(self,a):
        return math.sqrt(a)
