import math

class Statistics:
    def __init__(self):
        pass

    def add(self,a,b):
        return a+b
    def subtract(self,a,b):
        return a-b
    def multiply(self,a,b):
        return a*b
    def divide(self,a,b):
        return a/b
    def square(self,a):
        return a*a
    def sqaureRoot(self,a):
        return math.sqrt(a)
